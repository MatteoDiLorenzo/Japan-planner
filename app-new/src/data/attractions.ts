export const getTransportsBetweenCities = (from: string, to: string) => {
  return transportRoutes.filter(t => 
    t.from.toLowerCase().includes(from.toLowerCase()) && 
    t.to.toLowerCase().includes(to.toLowerCase())
  );
};

