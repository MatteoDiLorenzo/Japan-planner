// Calculate distance between two points (Haversine formula)
export const calculateDistance = (lat1: number, lng1: number, lat2: number, lng2: number): number => {
  const R = 6371; // Earth's radius in km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
            Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
            Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return Math.round(R * c * 10) / 10;
};

// Get transport options between two attractions
export const getTransportOptions = (from: Attraction, to: Attraction): TransportRoute[] => {
  const distance = calculateDistance(
    from.position.lat, from.position.lng,
    to.position.lat, to.position.lng
  );
  
  // If same city
  if (from.city === to.city) {
    // Walking distance
    if (distance < 2) {
      return [{
        id: `walk-${from.id}-${to.id}`,
        from: from.name,
        to: to.name,
        fromStation: from.name,
        toStation: to.name,
        departureTime: '-',
        arrivalTime: '-',
        duration: `${Math.round(distance * 15)}m`,
        price: 0,
        line: 'A piedi',
        type: 'walk',
        notes: `${distance} km`,
      }];
    }
    
    // Return city-specific transports
    return transportRoutes.filter(t => 
      (t.from.toLowerCase().includes(from.city) && t.to.toLowerCase().includes(to.city)) ||
      (t.from === from.name || t.to === to.name)
    );
  }
  
  // Different cities - return intercity transports
  return getTransportsBetweenCities(from.city, to.city);
};
